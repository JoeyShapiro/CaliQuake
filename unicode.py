
a = [ 0b11100010, 0b10010100, 0b10001100 ]

i = 0
uni = 0
while i < len(a):
    if a[0] & 0b10000000 == 0:
        print("1")
        uni = a[i]
        i += 1
    elif a[0] & 0b1110_0000 == 0b1100_0000:
        print("2")
        uni = (a[i] & 0b0001_1111) << 6 | (a[i+1] & 0b0011_1111)
        i += 2
    elif a[0] & 0b1111_0000 == 0b1110_0000:
        print("3")
        uni = (a[i] & 0b0000_1111) << 12 | (a[i+1] & 0b0011_1111) << 6 | (a[i+2] & 0b0011_1111)
        i += 3
    elif a[0] & 0b1111_1000 == 0b1111_0000:
        print("4")
        uni = (a[i] & 0b0000_0111) << 18 | (a[i+1] & 0b0011_1111) << 12 | (a[i+2] & 0b0011_1111) << 6 | (a[i+3] & 0b0011_1111)
        i += 4
    i += 1
    print(uni, chr(uni))
